export function getSortedArtikler() {
  return [...artikler].sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt))
}

export function getArtikkelBySlug(slug) {
  return artikler.find((artikkel) => artikkel.slug === slug)
}

export function getAndreArtikler(currentSlug, limit = 3) {
  return getSortedArtikler()
    .filter((artikkel) => artikkel.slug !== currentSlug)
    .slice(0, limit)
}
